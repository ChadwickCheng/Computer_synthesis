/*
插入排序
思想：逐个拿出元素，插入已排序部分的正确位置
初始：[5, 2, 8, 1, 9]

第1次：[5 | 2, 8, 1, 9]    →  2 < 5，2插左边  →  [2, 5 | 8, 1, 9]
第2次：[2, 5 | 8, 1, 9]    →  8 > 5，不动      →  [2, 5, 8 | 1, 9]
第3次：[2, 5, 8 | 1, 9]    →  1最小，插最左    →  [1, 2, 5, 8 | 9]
第4次：[1, 2, 5, 8 | 9]    →  9最大，不动      →  [1, 2, 5, 8, 9]

最坏：O(n^2) 初始逆序
最好：O(n) 已经有序
平均：O(n^2) 随机顺序
空间复杂度：O(1)
稳定
*/

/*
希尔排序
初始：[5, 9, 1, 8, 2, 6, 4, 7, 3]
常用gap：gap = n/2，然后 gap = gap/2，直到 gap=1
gap分组内部进行插入排序

gap=4：
  第0,4,8组：[5, 2, 3]        排序为 [2, 3, 5]
  第1,5组：  [9, 6]          排序为 [6, 9]
  第2,6组：  [1, 4]          排序为 [1, 4]
  第3,7组：  [8, 7]          排序为 [7, 8]
  → [2, 6, 1, 7, 3, 9, 4, 8, 5]

gap=2：
  第0,2,4,6,8组：[2, 1, 3, 4, 5]      排序为 [1, 2, 3, 4, 5]
  第1,3,5,7组：  [6, 7, 9, 8]        排序为 [6, 7, 8, 9]
  → [1, 6, 2, 7, 3, 8, 4, 9, 5]

gap=1：标准插入排序
  → [1, 2, 3, 4, 5, 6, 7, 8, 9]

最好：O(n log n)
最坏：O(n^2)
平均：O(n^1.5)
空间复杂度：O(1)
不稳定
*/

/*
选择排序
思想：每次从未排序部分找最小元素，放到已排序部分的末尾
初始：[5, 2, 8, 1, 9]

第1次：[未排 5, 2, 8, 1, 9]    →  找最小1，交换到位置0  →  [1 | 5, 2, 8, 9]
第2次：[已排1 | 5, 2, 8, 9]    →  找最小2，交换到位置1  →  [1, 2 | 5, 8, 9]
第3次：[已排1, 2 | 5, 8, 9]    →  找最小5，不动         →  [1, 2, 5 | 8, 9]
第4次：[已排1, 2, 5 | 8, 9]    →  找最小8，不动         →  [1, 2, 5, 8 | 9]
第5次：[已排1, 2, 5, 8 | 9]    →  只剩9                 →  [1, 2, 5, 8, 9]

时间复杂度：固定O(n^2)
空间复杂度：O(1)
不稳定（可能改变相等元素的相对位置）
*/

/*
冒泡排序
思想：相邻两个元素比较交换，每轮把最大的沉底，重复n-1轮
初始：[5, 2, 8, 1, 9]

第1轮：5<→2 [2,5,8,1,9] → 5<8 → 8<→1 [2,5,1,8,9] → 8<9 → [2, 5, 1, 8, 9 ↓]
第2轮：2<5 → 5<→1 [2,1,5,8,9] → 5<8 → [2, 1, 5, 8 ↓ 9]
第3轮：2<→1 [1,2,5,8,9] → 2<5 → [1, 2, 5 ↓ 8, 9]
第4轮：1<2 → [1, 2 ↓ 5, 8, 9]

最坏：O(n^2) 初始逆序
最好：O(n) 已有序
平均：O(n^2)
空间复杂度：O(1)
稳定
*/

/*
快速排序
思想：分治法。选基准分区，小的在左，大的在右，递归处理子区间
初始：[5, 2, 8, 1, 9]

选基准5：[2, 1 | 5 | 8, 9]
         ↓       ↓      ↓
        递归     基准   递归
    左边[2,1] 中间5  右边[8,9]
       ↓                ↓
    [1 | 2]         [8 | 9]
       ↓                ↓
      [1,2]           [8,9]

合并：[1, 2, 5, 8, 9]

最坏：O(n^2) 基准总是最大或最小
最好：O(n log n) 基准平衡分区
平均：O(n log n)
空间复杂度：O(log n) 递归栈
不稳定
*/

/*
归并排序
思想：分治法。先拆成单个元素，再逐层合并两个有序序列
初始：[5, 2, 8, 1, 9]

分解：[5] [2] [8] [1] [9]

合并：[2,5]    [1,8]    [9]
       ↓        ↓        ↓
    [1,2,5,8]  [9]
        ↓       ↓
    [1, 2, 5, 8, 9]

过程（两两合并）：
  [5,2]合并→[2,5]
  [8,1]合并→[1,8]
  [2,5]和[1,8]合并→[1,2,5,8]
  [1,2,5,8]和[9]合并→[1,2,5,8,9]

时间复杂度：固定O(n log n)
空间复杂度：O(n) 需要临时数组
稳定
*/

/*
堆排序
思想：建大顶堆，反复取堆顶放末尾，堆缩小后重新调整
初始：[5, 2, 8, 1, 9]

建大顶堆：       9
              /   \
             5     8
            / \
           2   1
        对应：[9, 5, 8, 2, 1]

第1次：取堆顶9，放末尾 → [1, 5, 8, 2, | 9] → 调整堆 → [8, 5, 1, 2]
第2次：取堆顶8，放末尾 → [2, 5, 1, | 8, 9] → 调整堆 → [5, 2, 1]
第3次：取堆顶5，放末尾 → [1, 2, | 5, 8, 9] → 调整堆 → [2, 1]
第4次：取堆顶2，放末尾 → [1, | 2, 5, 8, 9] → 调整堆 → [1]
第5次：取堆顶1 → [1, 2, 5, 8, 9]

时间复杂度：固定O(n log n)
空间复杂度：O(1)
不稳定
*/

