/*
1. 范式：符合某一级别的关系模式的集合
    构造数据库必须遵循一定规则
    不好的关系模式会出现：插入异常，删除异常，数据冗余大，修改复杂
    范式缺点：会导致查询效率降低；表会越小越多
2. 候选键：唯一标识一条记录的最小属性集
   主键：  某个能够唯一标识一条记录的最小属性集(候选码里选一个)
   主属性：候选键中的属性
   非主属性：不在候选键中的属性
   函数依赖：X->Y, X决定Y, Y依赖于X
3. 1NF：所有属性不可再分
4. 2NF：1NF 且 非主属性完全依赖于候选键
5. 3NF：2NF 且 非主属性不传递依赖于候选键 即 任意非主属性都不依赖其他非主属性
6. BCNF：消除任何属性对候选键的部分函数依赖和传递依赖
7. BCNF实现模式彻底分解，最高规范化，消除插入异常和删除异常
8. 解决方式：拆表，哪个依赖有问题就拆哪个表
*/

/**
核心概念的直观理解
1. 候选键（Candidate Key）
    最直观的理解：能唯一标识一条记录的最小"组合"
现实例子：假设你有一个"学生"表：
┌─────────┬────────┬────────┬──────────┐
│ 学号    │ 身份证号 │ 姓名   │ 班级     │
├─────────┼────────┼────────┼──────────┤
│ 001     │ 123XXX │ 张三   │ 高一1班  │
│ 002     │ 456XXX │ 李四   │ 高一2班  │
└─────────┴────────┴────────┴──────────┘
学号 能唯一确定一个学生 ✓ (候选键)
身份证号 也能唯一确定一个学生 ✓ (候选键)
姓名 不能，因为可能重名 ✗
学号+身份证号 也能唯一确定，但不是"最小" ✗
2. 主键（Primary Key）
    直观理解：从候选键中选一个"代表"来使用
从上面的例子中，选择 学号 作为主键（因为更方便查询）
3. 主属性 vs 非主属性
┌──────────────────┐
│    候选键属性     │  → 主属性（在某个候选键中）
│  (学号、身份证号) │
└──────────────────┘
         ↓
┌──────────────────┐
│   非主属性        │  → 不在任何候选键中（姓名、班级）
└──────────────────┘
4. 函数依赖（X→Y）
    直观理解：X的值决定了Y的值
示例：
    学号 → 姓名（知道学号就知道对应的姓名）✓
    学号 → 班级（知道学号就知道班级）✓
    姓名 → 学号（知道姓名不一定知道学号，可能重名）✗
5. 范式逐级递进
    1NF: 所有数据不可再分
        └─→ 不能在一个字段里存储多个值
        例如：班级字段不能是"高一1班、高一2班"

    2NF: 1NF + 非主属性完全依赖主键
        └─→ 要避免：非主属性只依赖候选键的一部分
        例如：选课表(学号, 课程号, 学生姓名)
            ❌ 学生姓名只依赖学号，不依赖课程号
            
        改进 → 拆成：选课(学号, 课程号) + 学生(学号, 姓名)

    3NF: 2NF + 非主属性之间无依赖关系
        └─→ 要避免：非主属性传递依赖于主键
        例如：学生(学号, 班级, 班主任)
            ❌ 班主任依赖于班级（传递依赖）
            
        改进 → 拆成：学生(学号, 班级) + 班级(班级, 班主任)
*/