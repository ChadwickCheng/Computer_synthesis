// ============ 答题指南 ============
/*
【常见错误点】
1. 自身连接记得起别名！不起别名会导致列名引用报错
2. WHERE子句写法的多表连接，连接条件数 = 表的数量 - 1
3. 子查询don't use ORDER BY（除非是最外层）
4. 外连接LEFT JOIN会保留左表的所有行，右表无匹配的填NULL
5. EXISTS只关心子查询有没有结果，不关心结果值本身
6. DELETE/UPDATE省略WHERE会作用全表，要小心！
7. INSERT子查询的SELECT列数和类型要匹配

【笛卡尔积的理解】
多表连接本质 = 笛卡尔积 × 消除不满足条件的行
即：FROM R, S WHERE 条件  ≡  σ(条件)(R × S)
这正是关系代数里连接的实现！
*/
